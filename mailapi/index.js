// === BACKUP FUNCTIONS ===
async function logFailedEmail(mail, error, timestamp) {
  try {
    const logEntry = {
      timestamp,
      mail,
      error: error.message,
      status: 'failed_to_save'
    };
    
    await appendFile(FAILED_EMAILS_LOG, JSON.stringify(logEntry) + '\n');
    console.log('📝 Failed email logged to backup file');
  } catch (logErr) {
    console.error('❌ Failed to log failed email:', logErr.message);
  }
}

// Function to retry failed emails from log
async function retryFailedEmails() {
  try {
    if (!fs.existsSync(FAILED_EMAILS_LOG)) {
      return { message: 'No failed emails log found' };
    }
    
    const logContent = fs.readFileSync(FAILED_EMAILS_LOG, 'utf8');
    const lines = logContent.trim().split('\n').filter(line => line);
    
    let retried = 0;
    let successful = 0;
    
    for (const line of lines) {
      try {
        const logEntry = JSON.parse(line);
        await Email.create(logEntry.mail);
        successful++;
      } catch (err) {
        console.error('❌ Retry failed for email:', err.message);
      }
      retried++;
    }
    
    // Clear the log file after processing
    if (successful > 0) {
      await writeFile(FAILED_EMAILS_LOG, '');
    }
    
    return { retried, successful, failed: retried - successful };
  } catch (err) {
    console.error('❌ Error retrying failed emails:', err.message);
    return { error: err.message };
  }
}

// === 📥 RECEIVE MAIL FROM HARAKA ===
app.post('/api/receive-mail', authKey, async (req, res) => {
  const mail = req.body;
  let socketSent = false;
  let dbSaved = false;
  let deviceEmailsSaved = false;
  
  try {
    // 🚀 STEP 1: Send to frontend first via real-time socket
    io.emit('new_mail', mail);
    socketSent = true;
    console.log('📡 Mail sent to frontend via socket');
    
    // 🗄️ STEP 2: Save to main Email collection
    await Email.create(mail);
    dbSaved = true;
    console.log('💾 Mail saved to main database');
    
    // 📱 STEP 3: Store in device-based collection for all devices that have this email
    const recipientEmail = Array.isArray(mail.to) ? mail.to[0] : mail.to;
    const devicesWithEmail = await DeviceEmail.find({ 
      email: recipientEmail, 
      type: 'generated' 
    });
    
    // Store received email for each device that has generated this email
    const deviceEmailPromises = devicesWithEmail.map(deviceEmail => 
      DeviceEmail.create({
        deviceId: deviceEmail.deviceId,
        email: recipientEmail,
        type: 'received',
        from: mail.from,
        subject: mail.subject,
        body: mail.body,
        receivedAt: new Date(mail.date)
      })
    );
    
    await Promise.all(deviceEmailPromises);
    deviceEmailsSaved = true;
    console.log(`📱 Mail saved to ${devicesWithEmail.length} device collections`);
    
    res.send('✅ Mail sent via socket and saved to database');
    
  } catch (err) {
    console.error('❌ Error in receive-mail:', err);
    
    // Detailed error reporting
    const status = {
      socketSent,
      dbSaved,
      deviceEmailsSaved,
      error: err.message
    };
    
    console.log('📊 Mail processing status:', status);
    
    // If socket was sent but database failed, log for manual recovery
     if (socketSent && !dbSaved) {
       const timestamp = new Date().toISOString();
       console.error('⚠️ CRITICAL: Mail sent to user but not saved to database!', {
         mail,
         timestamp
       });
       
       // Log to backup file for manual recovery
       await logFailedEmail(mail, err, timestamp);
       
       // Try to save again with retry logic
       try {
         await Email.create(mail);
         console.log('✅ Retry successful: Mail saved to database');
       } catch (retryErr) {
         console.error('❌ Retry failed:', retryErr.message);
       }
     }
    
    // Return appropriate response
    if (socketSent) {
      res.send('⚠️ Mail sent to user but database save failed');
    } else {
      res.status(500).send('❌ Error processing mail');
    }
  }
});

// === 📩 RANDOM EMAIL GENERATOR ===
app.get('/generate', authKey, async (req, res) => {
  const { deviceId } = req.query;
  let email;
  let attempts = 0;
  const maxAttempts = 10;
  
  // Keep generating until we find a unique email for this device
  do {
    const randomUser = crypto.randomBytes(4).toString('hex');
    const domain = ALLOWED_DOMAINS[Math.floor(Math.random() * ALLOWED_DOMAINS.length)];
    email = `${randomUser}@${domain}`;
    attempts++;
    
    // Check if email already exists for this device
    if (deviceId) {
      const existingEmail = await DeviceEmail.findOne({
        deviceId,
        email,
        type: 'generated'
      });
      
      if (!existingEmail) {
        // Email is unique, store it and break
        try {
          await DeviceEmail.create({
            deviceId,
            email,
            type: 'generated'
          });
          break;
        } catch (err) {
          console.error('Error storing generated email:', err);
        }
      }
    } else {
      // No device ID provided, just return the email
      break;
    }
  } while (attempts < maxAttempts);
  
  res.send({ email });
});

// === ✍️ MANUAL EMAIL GENERATOR ===
app.get('/generate/manual', authKey, async (req, res) => {
  const { username, domain, deviceId } = req.query;
  if (!username || !domain) return res.status(400).send('username and domain required');
  if (!ALLOWED_DOMAINS.includes(domain)) return res.status(400).send('domain not allowed');
  
  const email = `${username}@${domain}`;
  
  // Check if email already exists for this device
  if (deviceId) {
    try {
      const existingEmail = await DeviceEmail.findOne({
        deviceId,
        email,
        type: 'generated'
      });
      
      if (existingEmail) {
        return res.status(409).json({
          error: 'Email already exists',
          message: 'This email name already exists for your device. Please use a different name.',
          email: email
        });
      }
      
      // Email is unique, store it
      await DeviceEmail.create({
        deviceId,
        email,
        type: 'generated'
      });
    } catch (err) {
      console.error('Error storing generated email:', err);
      return res.status(500).send('Error creating email');
    }
  }
  
  res.send({ email });
});

